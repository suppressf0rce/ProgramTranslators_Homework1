import math


class RafMath:

    def sin(self, x):
        return math.sin(x)

    def cos(self, x):
        return math.cos(x)

    def tg(self, x):
        return math.tan(x)

    def ctg(self, x):
        return 1 / math.tan(x)

    def log(self, x):
        return math.log(x)

    def pow(self, x, y):
        return math.pow(x, y)

    def sqrt(self, x):
        return math.sqrt(x)
